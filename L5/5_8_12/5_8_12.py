import pandas as pd

# Журнал рибалки: MultiIndex (Місяць, Водойма)
index = pd.MultiIndex.from_tuples([
    ("Червень", "Озеро Синє"),
    ("Червень", "Річка Рось"),
    ("Липень",  "Озеро Синє"),
    ("Липень",  "Ставок Лісний"),
    ("Серпень", "Річка Рось"),
    ("Серпень", "Ставок Лісний"),
], names=["Місяць", "Водойма"])

df = pd.DataFrame(
    {"Кількість риби": [12, 7, 15, 9, 11, 6]},
    index=index
)

K = int(input("Виведіть перші K записів: "))
print(f"\nПерші {K} записів:")
print(df.head(K))

print("\nЗагальна статистика за все літо:")
print(df["Кількість риби"].agg(["mean", "min", "max"]).rename({
    "mean": "Середнє",
    "min":  "Мінімум",
    "max":  "Максимум",
}))

month = input("\nВведіть місяць (Червень / Липень / Серпень): ").strip()
if month in ["Червень", "Липень", "Серпень"]:
    total = df.loc[month, "Кількість риби"].sum()
    print(f"Сумарний вилов за {month}: {total} риб")
else:
    print("Невідомий місяць.")